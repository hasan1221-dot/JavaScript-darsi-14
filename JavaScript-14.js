let plusBtn = document.getElementById("plusBtn")
let minusBtn = document.getElementById("minusBtn")
let input = document.getElementById("input")
let natija = document.getElementById("natija")

input.value = 0;
let skladda_mavjud = 5
plusBtn.onclick = function oshirish (){
    let skladdagidan_oshdi = input.value >= skladda_mavjud;

    if (skladdagidan_oshdi) {
        minusBtn.disabled = true;
        natija.innerText = "Skladda boshqa qolmadi";
        minusBtn.disabled = false;
    } else {
        input.value = Number(input.value) + 1;
        minusBtn.disabled = false;
    }
};
minusBtn.onclick = function ayirish(){
    if (input.value <=0 ) {
        minusBtn.disabled = true;
    } else {
        input.value = Number(input.value) - 1;
    }
    natija.innerText = "";
};













































































































































































































































